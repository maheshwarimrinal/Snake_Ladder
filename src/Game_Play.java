    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String mp1 = p1.getText(),
               mp2 = p2.getText();
        int dec = num%2;
        int dice = (int)(Math.random()*10%6+1);
        DiceNo.setText(""+dice);
        int x = Integer.parseInt(DiceNo.getText());
        
            if(dec==0)                                                              //Player 1
            {
                dispname.setText(mp1+" Turn");
                num++;
                
                if(pos_1 == 0)                                                //Player One Chance Start
                {
                    if(x==1)
                    {

                        p1l22.setBackground(player_clr1.getBackground());
                        pos_1 = 22;
//                        pl1.setText(""+pos_1);
    //                    status.setText(p1.getText()+" started with 1");
                    }
                    else if(x==6)
                    {
                        p1l6.setBackground(player_clr1.getBackground());
                        pos_1 = 6;
      //                  pl1.setText(""+pos_1);
       //                 status.setText(p1.getText()+" started with 6");
                    }
                }
                else                                                                //Player One Further Chance
                {
                    player1_clr(pos_1);
                    
                    pos_1 += x;
                  //  test1.setText(""+pos_1);
             //       pl1.setText(""+pos_1);
                    player1(pos_1);
                }
            }                                                                       //Player One Condition End
            else                                                                      //Player 2
            {
                dispname.setText(mp2+" Turn");
                num++;
                
                if(pos_2 == 0)                                                  //Player Two Chance Start
                {
                    if(x==1)
                    {
                        p2l22.setBackground(player_clr2.getBackground());
                        pos_2 = 22;
    //                    pl2.setText(""+pos_2);
       //                 status.setText(p2.getText()+" started with 1");
                    }
                    else if(x==6)
                    {
                        p2l6.setBackground(player_clr2.getBackground());
                        pos_2 = 6;
       //                 pl2.setText(""+pos_2);
          //              status.setText(p2.getText()+" started with 6");
                    }
                }
                else                                                                    //Player Two Further Chance
                {
                    player2_clr(pos_2);
                    pos_2 += x;
       //             pl2.setText(""+pos_2);
                   // test2.setText(""+pos_2);
                    player2(pos_2);
                }
            }                                                                           //Player Two Condition End

    }//GEN-LAST:event_jButton3ActionPerformed

